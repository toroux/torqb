export const invokeNative = (eventName, data) => {
  if (typeof window !== "undefined" && typeof window.invokeNative === "function") {
    window.invokeNative(eventName, data)
  } else {
    console.log(`invokeNative called with event: ${eventName} and data:`, data)
  }
}

